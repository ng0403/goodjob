package com.crm.cp.standard.schedule.vo;

import java.sql.Date;

import org.springframework.stereotype.Component;

@Component
public class ScheduleVO {

	String schedule_id;
	String iuser_id;
	String schedule_type_cd;
	String schedule_nm;
	String schedule_cont;
	Date schedule_dt;
	String fst_reg_id_nm;
	Date fst_reg_dt;
	String fin_mdfy_id_nm;
	Date fin_mdfy_dt;
	String act_yn;
	
	public ScheduleVO(){}
	
	public ScheduleVO(String schedule_id, String iuser_id,
			String schedule_type_cd, String schedule_nm, String schedule_cont,
			Date schedule_dt, String fst_reg_id_nm, Date fst_reg_dt,
			String fin_mdfy_id_nm, Date fin_mdfy_dt, String act_yn) {
		super();
		this.schedule_id = schedule_id;
		this.iuser_id = iuser_id;
		this.schedule_type_cd = schedule_type_cd;
		this.schedule_nm = schedule_nm;
		this.schedule_cont = schedule_cont;
		this.schedule_dt = schedule_dt;
		this.fst_reg_id_nm = fst_reg_id_nm;
		this.fst_reg_dt = fst_reg_dt;
		this.fin_mdfy_id_nm = fin_mdfy_id_nm;
		this.fin_mdfy_dt = fin_mdfy_dt;
		this.act_yn = act_yn;
	}
	
	public String getSchedule_id() {
		return schedule_id;
	}
	public void setSchedule_id(String schedule_id) {
		this.schedule_id = schedule_id;
	}
	public String getIuser_id() {
		return iuser_id;
	}
	public void setIuser_id(String iuser_id) {
		this.iuser_id = iuser_id;
	}
	public String getSchedule_type_cd() {
		return schedule_type_cd;
	}
	public void setSchedule_type_cd(String schedule_type_cd) {
		this.schedule_type_cd = schedule_type_cd;
	}
	public String getSchedule_nm() {
		return schedule_nm;
	}
	public void setSchedule_nm(String schedule_nm) {
		this.schedule_nm = schedule_nm;
	}
	public String getSchedule_cont() {
		return schedule_cont;
	}
	public void setSchedule_cont(String schedule_cont) {
		this.schedule_cont = schedule_cont;
	}
	public Date getSchedule_dt() {
		return schedule_dt;
	}
	public void setSchedule_dt(Date schedule_dt) {
		this.schedule_dt = schedule_dt;
	}
	public String getFst_reg_id_nm() {
		return fst_reg_id_nm;
	}
	public void setFst_reg_id_nm(String fst_reg_id_nm) {
		this.fst_reg_id_nm = fst_reg_id_nm;
	}
	public Date getFst_reg_dt() {
		return fst_reg_dt;
	}
	public void setFst_reg_dt(Date fst_reg_dt) {
		this.fst_reg_dt = fst_reg_dt;
	}
	public String getFin_mdfy_id_nm() {
		return fin_mdfy_id_nm;
	}
	public void setFin_mdfy_id_nm(String fin_mdfy_id_nm) {
		this.fin_mdfy_id_nm = fin_mdfy_id_nm;
	}
	public Date getFin_mdfy_dt() {
		return fin_mdfy_dt;
	}
	public void setFin_mdfy_dt(Date fin_mdfy_dt) {
		this.fin_mdfy_dt = fin_mdfy_dt;
	}
	public String getAct_yn() {
		return act_yn;
	}
	public void setAct_yn(String act_yn) {
		this.act_yn = act_yn;
	}

}
