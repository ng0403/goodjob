package com.crm.cp.test.code.vo;

public class CodeVo {
	private String code;
	private String code_name;
	private String display_seq;
	private String code_desc;
	private String active_flg;
	
	public CodeVo(){}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getCode_name() {
		return code_name;
	}

	public void setCode_name(String code_name) {
		this.code_name = code_name;
	}

	public String getDisplay_seq() {
		return display_seq;
	}

	public void setDisplay_seq(String display_seq) {
		this.display_seq = display_seq;
	}

	public String getCode_desc() {
		return code_desc;
	}

	public void setCode_desc(String code_desc) {
		this.code_desc = code_desc;
	}

	public String getActive_flg() {
		return active_flg;
	}

	public void setActive_flg(String active_flg) {
		this.active_flg = active_flg;
	}

}
